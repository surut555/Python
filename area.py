#ตัวแปรชนิดค่าคงที่ (Constant)
pi = 3.14 #สร้างตัวแปร pi เป็นค่าคงที่
area = pi *5*5 #คูณค่าตัวแปร pi เพื่อหาเส้นรอบวงกลม
circumference = 2 * pi * 10 #คูณค่าตัวแปร pi เพื่อหาเส้นรอบวงกลม
print("พื้นที่วงกลม =",area) #แสดงผลลัพธ์ตัวแปร arec
print("เส้นรอบวงกลม",circumference)


#ตัวแปร(Integer) int = เป็นจำนวนเต็ม
num1 = 567 #สร้างตัแปร num1 เป็นชนิดข้อมูลจำนวนเต็ม
num2 = 6576 #สร้างตัแปร num2 เป็นชนิดข้อมูลจำนวนเต็ม
