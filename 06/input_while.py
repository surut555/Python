#การเขียนโปรแกรมแบบทำซ้ำด้วยคำสั่ง while โดยการรับค่าจะผู้ใช้งาน
i = 1
num = []
while i <= 10:
    
    n = int(input("กรุณาป้อนตัวครั้งที่ %d :"%i))
    num.append(n)
    i = i +1
print("กรุณาป้อนตัวเลขครั้งที่",num)
print("กรุณาป้อนตัวเลขค่าที่น้อยที่สุด",min(num))
print("กรุณาป้อนตัวเลขที่มีค่ามากที่สุด",max(num))
print("ผลรวมตัวเลขทั้งหมด",sum(num))
