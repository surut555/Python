#การเขียนโปรแกรมควบคุมทิศทางการทำงานแบบลำดับคำนวณพื้นที่สี่เหลี่ยมผืนผ้า
x = float(input("กรุณาป้อนความยาวของสี่เหลี่ยม = :"))
#รอรับค่าการป้อนข้อมูลด้านความยาวแล้วแปลงเป็นจำนวนทิศนิยม
y = float(input("กรุณาป้อนความกว้างของสี่เหลี่ยมผืนผ้า = :"))
#รอรับค่าการป้อนข้อมูลด้านความกว้างแล้วแปลงเป็นจำนวนทิศนิยม
z = x * y #ดำเนินการคูณค่าตัวแปร x กับ y ผลลัพธ์เก็บไว้ในตัวแปร z
print("พื้นที่สี่เหลี่ยมผืนผ้า = ",z)#แสดงผลลัพธ์ตัวแปร z